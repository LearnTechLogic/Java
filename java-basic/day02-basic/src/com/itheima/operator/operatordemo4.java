package com.itheima.operator;

public class operatordemo4 {
    public static void main(String[] args) {
        print1();
    }
    //学习关系运算符，给出一段代码学习
    public static void print1() {
        int a = 10;
        int b = 20;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }

}
